def task_2_4():
    """
    Обчислює вираз на основі вводу користувача,
    а потім ділить результат на випадкове ціле число від 3 до 7.
    """
    print("--- Завдання 2.4 ---")
    while True:
        try:
            a = int(input("Введіть значення для 'a': "))
            b = int(input("Введіть значення для 'b': "))

            up = math.sqrt(a**3 + 2.5 * b)
            down = 0.5 * (a + b)

            if down == 0:
                print("Помилка: Ділення на нуль.")
                return

            y = up / down
            print(f"y = {y}")

            rand_divisor = random.randint(3, 7)
            final_result = y / rand_divisor
            print(f"y, поділене на випадкове число ({rand_divisor}): {final_result}")
            return

        except ValueError:
            print("Неправильний ввід. Будь ласка, введіть ціле число.")

    perepon()


